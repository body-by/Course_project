const allLang = ['us', 'ru', 'by']; //all valid lang



const currencyBox = document.getElementById('currency_box');
const footerContentList = document.getElementById('footerContentList');
currencyBox.addEventListener('mouseover', () => {
    footerContentList.style.display = 'flex';

    
});
currencyBox.addEventListener('mouseout', () => {
    footerContentList.style.display = 'none';
    
});



const changePlace = document.getElementById("us");
let local_now  = 'us';
    
currencyBox.addEventListener("click", function(event) {
    const clickedElement = event.target.closest(".footerContent");
    if (clickedElement && clickedElement !== changePlace) {
        // Получаем текст и иконку для валюты USD
        const usdText = changePlace.querySelector("span").textContent;
        const usdIcon = changePlace.querySelector("svg").outerHTML;
        // Получаем текст и иконку для выбранной валюты
        const clickedText = clickedElement.querySelector("span").textContent;
        const clickedIcon = clickedElement.querySelector("svg").outerHTML;
        // Меняем текст и иконку для валюты USD
        changePlace.querySelector("span").textContent = clickedText;
        changePlace.querySelector("svg").outerHTML = clickedIcon;
        // Меняем текст и иконку для выбранной валюты
        clickedElement.querySelector("span").textContent = usdText;
        clickedElement.querySelector("svg").outerHTML = usdIcon;
         
        switch(clickedText){
            case 'English':
                local_now = 'us';
                changeURLLanguage(local_now)
                break;
                
            case 'Русский':
                local_now = 'ru';
                changeURLLanguage(local_now)
                    break;
            case 'Беларуская':
                local_now = 'by';
                changeURLLanguage(local_now)
                break;

            default:
                   
            
        }
    }
});

// Функция для перестановки значений между блоками
function swapValues(hash) {
    const changePlace = document.getElementById("us");
    const targetElement = document.getElementById(hash);

    if (changePlace && targetElement) {
        // Получаем текущий текст и SVG из блока с id "us"
        const usText = changePlace.querySelector("span").textContent;
        const usSvg = changePlace.querySelector("svg").outerHTML;

        // Получаем текст и SVG из блока с id, соответствующему hash
        const targetText = targetElement.querySelector("span").textContent;
        const targetSvg = targetElement.querySelector("svg").outerHTML;

        // Меняем текст и SVG местами
        changePlace.querySelector("span").textContent = targetText;
        changePlace.querySelector("svg").outerHTML = targetSvg;
        targetElement.querySelector("span").textContent = usText;
        targetElement.querySelector("svg").outerHTML = usSvg;
    }
}






// Функция, которая будет вызываться при изменении переменной local_now
//перенаправить на url с указанием язвыка  
const changeURLLanguage = (lang) => {
    location.href = window.location.pathname + '#' + lang;
    location.reload();
};

function changeLanguage(){
    let hash = window.location.hash;
    hash = hash.substr(1);
    if(!allLang.includes(hash)){  
        location.href = window.location.pathname + '#us' ; //default value
        location.reload();
    }
    else{
        swapValues(hash);
        document.querySelector('title').innerHTML = "CIA - " + langArr["title"][hash];
        for (let key in langArr) {
            let elem = document.querySelector('.lng-' + key);
            if (elem) {
            elem.innerHTML = langArr[key][hash];
            }
            if(key === "search"){
                elem.placeholder = langArr[key][hash];
            }
        }
       

    }
    console.log(hash);
}
changeLanguage();



